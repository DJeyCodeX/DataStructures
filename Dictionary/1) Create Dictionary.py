
# Empty Dictionary
# Time Complexity - O(1)
# Space Complexity - O(1)
my_dict = dict()
print(len(my_dict))
print(my_dict)
print("-----------------------------------")

# Empty Dictionary
# Time Complexity - O(1)
# Space Complexity - O(1)
my_dict = {}
print(len(my_dict))
print(my_dict)
print("-----------------------------------")

# Non-Empty Dictionary
# Time Complexity - O(n)
# Space Complexity - O(n)
my_dict = dict(one='uno', two='dos', three='tres')
print(len(my_dict))
print(my_dict)
print("-----------------------------------")

# Non-Empty Dictionary
# Time Complexity - O(n)
# Space Complexity - O(n)
my_dict = {'one': 'uno', 'two': 'dos', 'three': 'tres'}
print(len(my_dict))
print(my_dict)
print("-----------------------------------")
